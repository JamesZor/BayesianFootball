# src/experiments/types.jl

using ..Models: AbstractFootballModel
using ..Data: AbstractSplitter, AbstractSplitMetaData
using ..Training: TrainingConfig

export ExperimentConfig, ExperimentResults

"""
    ExperimentConfig

The immutable recipe for an experiment. 
"""
Base.@kwdef struct ExperimentConfig
    name::String
    model::AbstractFootballModel
    splitter::AbstractSplitter 
    training_config::TrainingConfig 
    tags::Vector{String} = String[]
    description::String = ""
    save_dir::String = "./data/experiments"
end

"""
    ExperimentResults
Container for artifacts generated by a run.
"""
struct ExperimentResults
    config::ExperimentConfig
    # training_results::Vector{Tuple{Any, Any}} 
    training_results::TrainingResults
    vocabulary::Any # Relaxed type to avoid circular dependency issues # HACK: not needed - using nothing
    save_path::String
end

