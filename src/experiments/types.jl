# src/experiments/types.jl

using ..Models: AbstractFootballModel
using ..Data: AbstractSplitter, AbstractSplitMetaData
using ..Training: TrainingConfig

export ExperimentConfig, ExperimentResults

"""
    ExperimentConfig

The immutable recipe for an experiment. 
"""
Base.@kwdef struct ExperimentConfig
    name::String
    model::AbstractFootballModel
    splitter::AbstractSplitter 
    training_config::TrainingConfig 
    tags::Vector{String} = String[]
    description::String = ""
    save_dir::String = "./experiments"
end

"""
    ExperimentResults
Container for artifacts generated by a run.
"""
struct ExperimentResults
    config::ExperimentConfig
    training_results::Vector{Tuple{Any, Any}} 
    vocabulary::Any # Relaxed type to avoid circular dependency issues
    save_path::String
end

# --- Pretty Printing (Base.show) ---

function Base.show(io::IO, ::MIME"text/plain", config::ExperimentConfig)
    # Header
    printstyled(io, "🧪 ExperimentConfig: ", color=:magenta, bold=true)
    printstyled(io, config.name, "\n", color=:white, bold=true)
    
    # Model
    printstyled(io, "   ├── 🧠 Model: ", color=:light_black)
    printstyled(io, nameof(typeof(config.model)), "\n", color=:cyan)
    
    # Splitter
    printstyled(io, "   ├── ✂️  Splitter: ", color=:light_black)
    printstyled(io, nameof(typeof(config.splitter)), "\n", color=:yellow)
    # We can inspect fields of the splitter if known
    if hasproperty(config.splitter, :window_col)
        println(io, "   │    └── Window: $(config.splitter.window_col)")
    end
    
    # Training
    printstyled(io, "   ├── 🏋️  Training: ", color=:light_black)
    tc = config.training_config
    println(io, "")
    println(io, "   │    ├── Sampler: $(nameof(typeof(tc.sampler))) (N=$(tc.sampler.n_samples))")
    println(io, "   │    └── Strategy: $(nameof(typeof(tc.strategy)))")

    # Metadata
    printstyled(io, "   └── 🏷️  Tags: ", color=:light_black)
    println(io, isempty(config.tags) ? "None" : join(config.tags, ", "))
end
